        : null;
    token = json['token'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (deliveryBoy != null) {
      data['deliveryBoy'] = deliveryBoy!.toJson();
    }
    data['token'] = token;
    return data;
  }
}

