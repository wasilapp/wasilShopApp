class Category {
  int? id;
  Name? title;
  Name? description;
  double? commesion;
  String? imageUrl;
  String? type;
  int? deliveryFee;
  double? expeditedFees;
  double ?schedulerFees;
  String? startWorkTime;
  String? endWorkTime;
  int? active;
  String? createdAt;
  String? updatedAt;

  Category(
      {this.id,
        this.title,
        this.description,
        this.commesion,
        this.imageUrl,
        this.type,
        this.deliveryFee,
        this.expeditedFees,
        this.schedulerFees,
        this.startWorkTime,
        this.endWorkTime,
        this.active,
        this.createdAt,
        this.updatedAt});

  Category.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    title = json['title'] != null ? Name.fromJson(json['title']) : null;
    description = json['description'] != null
        ? Name.fromJson(json['description'])
        : null;
    commesion = json['commesion'];
    imageUrl = json['image_url'];
    type = json['type'];
    deliveryFee = json['delivery_fee'];
    expeditedFees = json['expedited_fees'];
    schedulerFees = json['scheduler_fees'];
    startWorkTime = json['start_work_time'];
    endWorkTime = json['end_work_time'];
    active = json['active'];
    createdAt = json['created_at'];
    updatedAt = json['updated_at'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['id'] = id;
    if (title != null) {
      data['title'] = title!.toJson();
    }
    if (description != null) {
      data['description'] = description!.toJson();
    }
    data['commesion'] = commesion;
    data['image_url'] = imageUrl;
    data['type'] = type;
    data['delivery_fee'] = deliveryFee;
    data['expedited_fees'] = expeditedFees;
    data['scheduler_fees'] = schedulerFees;
    data['start_work_time'] = startWorkTime;
    data['end_work_time'] = endWorkTime;
    data['active'] = active;
    data['created_at'] = createdAt;
    data['updated_at'] = updatedAt;
    return data;
  }
}
